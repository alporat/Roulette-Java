    // Closes bets and spins the wheel
    private void spinButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_spinButtonActionPerformed
        // Disables/Hides the bet buttons, if they have no chips placed on them
        for (Component component : betPosPanel.getComponents()) {
            if (!betButtons.contains(component)){
                component.setEnabled(false);
            }
        }
        betPosEnabled = false;
        // Removes the selection chips' highlight
        chip10Button.setBorderPainted(false);
        chip100Button.setBorderPainted(false);
        chip1000Button.setBorderPainted(false);
        chip5000Button.setBorderPainted(false);
        selectedChip = 10;
        // Clears the text outputs
        winnerNumberOutput.setText("");
        winAmountOutput.setText("");
        // Updates/Refreshes the UI graphics in order to show above changes
        update(getGraphics());
        // Spins the wheel and stores the number that the ball stopped at to winnerNumber
        int winnerNumber = wheelSpin();
        winnerNumberOutput.setText("Winner Number is " + winnerNumber + "!");
        // Calculates the winnings and if won any, adds it to the chip balance
        chips += calculateWin(winnerNumber);
        // Clears the bets
        betAmount = 0;
        winAmount = 0;
        bets.clear();
        // Removes the chips on the table
        for (Component component : betPosPanel.getComponents()) {
            if (betButtons.contains(component)) {
                try{
                    if (component.getName().contains("$")){
                        ((JButton)component).setIcon(new ImageIcon(ImageIO.read(getClass().getResource("/roulette/selection2.png"))));
                    } else {
                        ((JButton)component).setIcon(new ImageIcon(ImageIO.read(getClass().getResource("/roulette/selection.png"))));
                    }
                } catch (IOException ex) {
                    Logger.getLogger(RouletteUI.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            component.setEnabled(false);
        }
        betButtons.clear();
        updateBalance();
    }//GEN-LAST:event_spinButtonActionPerformed

