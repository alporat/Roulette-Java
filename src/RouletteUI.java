    // Custom class to save/record the bets
    class Bet {
        int number; // Betted number
        int bet; // Bet amount
        double x; // Winning multiplier
        Bet(int _number, int _bet, double _x) {
            number = _number;
            bet = _bet;
            x = _x;
        }
    }
    
    // Runs when a bet has been placed
    void betPos(int[] numbers, double x, JButton button){
        if(selectedChip<=chips){ // If player has enough balance
            for (int number : numbers) { // Adds the bet to the "bets" array
                bets.add(new Bet(number, selectedChip, x));
            }
            chips -= selectedChip;
            betAmount += selectedChip;
            
            // Changes the icon of the bet button to the selected chip
            try {
                button.setIcon(new ImageIcon(ImageIO.read(getClass().getResource("/roulette/chipPlaced"+String.valueOf(selectedChip)+".png"))));
            } catch (IOException ex) {
                Logger.getLogger(RouletteUI.class.getName()).log(Level.SEVERE, null, ex);
            }
            betButtons.add(button); // Saves the selected button
            
            winAmountOutput.setForeground(Color.WHITE);
            winAmountOutput.setText("Total Bet Amount: " + String.valueOf(betAmount) + " Chips");
            updateBalance();
        } else {
            winAmountOutput.setForeground(Color.WHITE);
            winAmountOutput.setText("Not Enough Balance!");
        }
    }
    // Runs when a chip has been selected
    void chipSelection(int chipNo){
        // Turns the bet buttons on, if the bet buttons are not enabled/visible
        if(!betPosEnabled){
            for (Component component : betPosPanel.getComponents()) {
                component.setEnabled(true);
            }
            winAmountOutput.setForeground(Color.WHITE);
            winAmountOutput.setText("Now Select Your Bet");
            betPosEnabled = true;
        }
        switch (chipNo) {
            case 10:
                chip10Button.setBorderPainted(true);
                chip100Button.setBorderPainted(false);
                chip1000Button.setBorderPainted(false);
                chip5000Button.setBorderPainted(false);
                selectedChip = 10;
                break;
            case 100:
                chip10Button.setBorderPainted(false);
                chip100Button.setBorderPainted(true);
                chip1000Button.setBorderPainted(false);
                chip5000Button.setBorderPainted(false);
                selectedChip = 100;
                break;
            case 1000:
                chip10Button.setBorderPainted(false);
                chip100Button.setBorderPainted(false);
                chip1000Button.setBorderPainted(true);
                chip5000Button.setBorderPainted(false);
                selectedChip = 1000;
                break;
            case 5000:
                chip10Button.setBorderPainted(false);
                chip100Button.setBorderPainted(false);
                chip1000Button.setBorderPainted(false);
                chip5000Button.setBorderPainted(true);
                selectedChip = 5000;
                break;
        }
    }
    // Runs when wheel spin ends to calculate the winnings
    double calculateWin(int winnerNumber){
        winAmount = 0;
        for (int i = 0; i < bets.size(); i++) {
            if (bets.get(i).number == winnerNumber) {
                // Adds the betted amount times the multiplier to winAmount
                winAmount += bets.get(i).bet * bets.get(i).x;
            }
        }
        winAmount -= betAmount; // Used to calculate profit/loss
        if (winAmount < 0){
            winAmountOutput.setForeground(Color.red);
            winAmountOutput.setText("You Have Lost " + (int)-winAmount + " chips!");
        } else {
            winAmountOutput.setForeground(Color.green);
            winAmountOutput.setText("You Have Won " + (int)winAmount + " chips!");
        }
        return winAmount+betAmount;
    }
    
