    // Running the wheel spin animation, stopping at a random value and returning that value
    int wheelSpin(){
        Random r = new Random();
        // Enable the ball image
        rouletteBall.setEnabled(true);
        
        // Resetting wheel and ball's image
        try {
            rouletteWheel.setIcon(new ImageIcon(ImageIO.read(getClass().getResource("/roulette/roulettewheel.png"))));
            rouletteBall.setIcon(new ImageIcon(ImageIO.read(getClass().getResource("/roulette/rouletteball.png"))));
        } catch (IOException ex) {
            Logger.getLogger(RouletteUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        // equals one spin of the wheel
        double oneSpin = 6.28;
        // Stores: wheel and ball's last rotation value, ball stopped index, wheel spin stage number
        double endPlace[], placeStart[]; 
        placeStart = new double[4];
        placeStart[0] = stopPlaces[0];
        placeStart[1] = 1;
        placeStart[2] = 0;
        placeStart[3] = 0;
        
        // First Stage: Start spinning the wheel (only spin wheel)
        double wheelTurnTime1 = oneSpin * (1.0 + 0.7 * r.nextDouble()); // Amount of times the wheel will spin (random double)
        double wheelSpeed1 = 1.3 + 0.3 * r.nextDouble(); // Random double between 1.3 and 1.6 which declares the speed of the wheel
        // Runs the first stage of the animation
        // Arguments: wheel's starting rotation, times the wheel will turn, wheel's speed, ball's speed, turn wheel (true), turn ball (false)
        endPlace = wheelSpinTo(placeStart, wheelTurnTime1, wheelSpeed1, 0, true, false);
        
        // Second Stage: Start spinning the ball (spin both)
        double ballSpeed1 = 1.5 + 0.4 * r.nextDouble();
        endPlace = wheelSpinTo(endPlace, wheelTurnTime1, wheelSpeed1, ballSpeed1, true, true);
        
        // Third Stage: Start slowing both (spin both slowly)
        double wheelSpeed2 = 0.6 + 0.2 * r.nextDouble();
        double ballSpeed2 = 0.3 + 0.1 * r.nextDouble();
        double wheelTurnTime2 = oneSpin * (0.6 + 0.8 * r.nextDouble());
        endPlace = wheelSpinTo(endPlace, wheelTurnTime2, wheelSpeed2, ballSpeed2, true, true);
        
        // Forth Stage: Stop the ball (match the wheel for the ball)
        // Gets the winner number by the output gained from third stage, and compares it to the "slots" array
        int winnerNumber = slots[((int)endPlace[2])%37];
        double wheelTurnTime3 = oneSpin + 2;
        // Setting ball's speed -1 makes it follow the wheel (stops the ball)
        endPlace = wheelSpinTo(endPlace, wheelTurnTime3, wheelSpeed2, -1, true, true);
        // Saves the stopped rotation of the wheel and ball, so that when spinned again, it starts where it is left
        stopPlaces[0] = endPlace[0];
        stopPlaces[1] = endPlace[1];

        
        // Changing both the wheel's and the ball's icon to their stopped position in the animation
        // Step 1: Creating the BufferedImages and getting their Graphics2D
        BufferedImage imgWheel = null;
        BufferedImage imgBall = null;
        BufferedImage imgBall2 = null;
        try {
            imgWheel = ImageIO.read(getClass().getResource("/roulette/roulettewheel.png"));
            imgBall = ImageIO.read(getClass().getResource("/roulette/roulettewheelempty.png"));
            imgBall2 = ImageIO.read(getClass().getResource("/roulette/rouletteballrotation.png"));
        } catch (IOException ex) {
            Logger.getLogger(RouletteUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        Graphics2D gWheel = imgWheel.createGraphics();
        Graphics2D gBall = imgBall.createGraphics();
        
        // Step 2: Rotating the wheel to its last rotation value set after the animation
        gWheel.rotate(stopPlaces[0], 262, 262);
        
        // Step 3: Setting the image ball's graphics to existant roulette ball
        Graphics2D g1 = (Graphics2D) rouletteBall.getGraphics();
        gBall.setComposite(g1.getComposite());
        
        // Step 4: Rotating the ball to its last rotation value set after the animation
        AffineTransform transf = new AffineTransform();
        transf.translate(262, 262);
        transf.rotate(-stopPlaces[1]);
        transf.translate(-30, -30);
        g1.setTransform(transf);
        
        // Step 5: Applying the rotations to the BufferedImages created
        gBall.drawImage(imgBall2, transf, null);
        rouletteBall.paint(g1);
        g1.dispose();
        gBall.dispose();
        rouletteWheel.paint(gWheel);
        gWheel.dispose();
        
        // Step 6: Setting both wheel and ball's icons to the rotated BufferedImages
        rouletteBall.setIcon(new ImageIcon(imgBall));
        rouletteWheel.setIcon(new ImageIcon(imgWheel));
        
        return winnerNumber;
    }
    
    // Using the given speed arguments and amount of turn times, applies the turn animations
    double[] wheelSpinTo(double[] startPlaces, double to, double wheelSpeed, double ballSpeed, boolean spinWheel, boolean spinBall){
        double endPlace = 0;
        // Starting value "i" is set to the wheel's last rotation value divided by wheelSpeed, so that
        //    when applying the speed for the first time, it equals to the wheel's last rotation value
        // The amount of times the loop will continue is set to "to" plus the value set for "i"
        //    so that this loop continues for the set amount of times by the given argument "to"
        for (double i = startPlaces[0]/wheelSpeed; i < to+startPlaces[0]/wheelSpeed; i+=0.01) {
            if (spinBall){
                if (ballSpeed == -1){
                    // Makes it follow the wheel's turns so that it looks like the ball stopped
                    ballSpinAnimation((startPlaces[1]-((i-startPlaces[0]/wheelSpeed)*wheelSpeed)));
                } else {
                    // Turns the ball at its last rotation value and applies the given ballSpeed argument
                    ballSpinAnimation(((startPlaces[1]/ballSpeed)+(i-startPlaces[0]/wheelSpeed))*ballSpeed);
                }
            }
            if (spinWheel){
                // Spins the wheel by multiplying "i" and its given wheelSpeed argument
                wheelSpinAnimation(i*wheelSpeed);
            }
            // Waits 2 milisecconds
            try {
                TimeUnit.MILLISECONDS.sleep(2);
            } catch (InterruptedException ex) {
                Logger.getLogger(RouletteUI.class.getName()).log(Level.SEVERE, null, ex);
            }
            // Saves the last rotation value
            endPlace = (i);
        }
        double a[] = new double[4]; // Returning Values:
        a[3] = startPlaces[3]+1; // Wheel spin stage number
        a[2] = 0; // Ball stopped index
        a[0] = endPlace*wheelSpeed; // Wheel's last rotation value
        if (spinBall){
            // Ball's last rotation value
            a[1] = ((startPlaces[1]/ballSpeed)+(endPlace-startPlaces[0]/wheelSpeed))*ballSpeed;
            // Formula for finding the ball stopped index ballRotate=-wheelRotate+0.1+0.1698*z
            // So, z = (ballRotate+wheelRotate-0.1)/0.1698
            // z is the slots array index, which is the winner number's index number
            if (a[3] == 3){ // If this is the stage 3
                // Applies the formula above
                double index = ((a[0]+a[1]-0.1)/0.1698);
                // If the index's decimal number is less than or equals to 0.3, makes that
                //    decimal number a whole, if not, makes it whole and adds 1 more. This
                //    is done to make the animation look smooth when stopping the ball.
                double diff = index - (int)index;
                if (diff <= 0.3){
                    a[2] = (int)index;
                } else {
                    a[2] = ((int)index)+1;
                }
                // Sets the ball's last rotation to where the ball is on the index, using the formula above
                a[1] = -a[0]+0.1+0.1698*a[2];
            } else if (a[3] == 4) { // Else if this is the stage 4
                // Sets the ball's last rotation to be ready for applying into BufferedImage
                a[1] = startPlaces[1]-((endPlace-startPlaces[0]/wheelSpeed)*wheelSpeed);
            }
        } else {
            // If not spinning the ball, sets the ball's last rotation value to 1
            a[1] = 1;
        }
        return(a);
    }
    
    // Turns the wheel using the given rotation value argument, then paints it on top of the wheel
    void wheelSpinAnimation(double r){
        Graphics2D g1 = (Graphics2D)rouletteWheel.getGraphics();
        AffineTransform transf = new AffineTransform();
        transf.translate(262, 262);
        transf.rotate(r);
        transf.translate(-262, -262);
        g1.setTransform(transf);
        rouletteWheel.paint(g1);
    }
    // Turns the ball using the given rotation value argument, inverted, then paints it on top of the wheel
    void ballSpinAnimation(double r){
        Graphics2D g1 = (Graphics2D)rouletteBall.getGraphics();
        AffineTransform transf = new AffineTransform();
        transf.translate(262, 262);
        transf.rotate(-r);
        transf.translate(-30, -30);
        g1.setTransform(transf);
        rouletteBall.paint(g1);
    }
    
