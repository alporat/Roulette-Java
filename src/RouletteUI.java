    // Saves the chip amount to the data file
    void saveBalance() {
        try {
            File dataFile = new File("src/roulette/data");
            dataFile.delete();
            dataFile.createNewFile();
            FileWriter writer = new FileWriter("src/roulette/data");
            writer.write(String.valueOf(chips));
            writer.close();
        } catch (IOException ex) {
            Logger.getLogger(RouletteUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    // Gets the chip amount from the data file
    double getBalance() {
        double chipsValue = chipsStartBalance;
        try {
            FileReader reader = new FileReader("src/roulette/data");
            BufferedReader bufferedReader = new BufferedReader(reader);
            try {
                chipsValue = Double.parseDouble(bufferedReader.readLine());
                // If player has less than 10 chips, resets the chip amount to 100
                if ((int)chipsValue < 10){
                    chipsValue = 100;
                }
            // If data file has been edited wrongly (has letters in it), resets the balance
            } catch (NumberFormatException e) {
                chipsValue = chipsStartBalance;
            }
            reader.close();
        // If data file has not been created, sets the balance to the default value
        } catch (FileNotFoundException ex) {
            chipsValue = chipsStartBalance;;
        } catch (IOException ex) {
            Logger.getLogger(RouletteUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        return chipsValue;
    }
    // Updates/Refreshes the balance on UI
    void updateBalance(){
        balanceOutput.setText(String.valueOf((int) chips));
    }
    
